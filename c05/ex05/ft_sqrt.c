int	ft_sqrt(int nb)
{
	int	x;
	int	y;

	if (nb <= 0)
	{
		return (0);
	}
	x = nb;
	y = 1;
	while (x > y)
	{
		x = (x + y) / 2;
		y = nb / x;
	}
	if (x * x == nb)
	{
		return (x);
	}
	else
	{
		return (0);
	}
}
